<B> Acquiring a Learning License is the first step to towards getting the Driving license that allows you to drive in India. The learning license is issued by the <B>Regional Transport Office (RTO) </B>and acts as a provisional and restricted license.  
<br> <br>
To get a Learner's License, one must be familiar with rules and regulations related to road safety. It is issued to the candidate after passing an online test about road rules. It also allows you time to undergo training required to drive a motor vehicle. You must obtain a Learner's License for the relevant class of vehicle before you can get a Driving License.
<br> 
You can practice driving with a person holding a permanent Driving License, only if you have a learner's license.</B>
