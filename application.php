<B><U> Eligibility criteria for Learning License </U></B>
<br><br>
<U>General</U><br>
The Learner's License applicant must be well versed with traffic rules & regulations.<br>
The applicant must have valid address proof and age proof documents.
<br> <br>
<U>Motorcycles without gear with up to 50cc capacity</U><br>
The candidate must be at-least 16 years old. Consent from guardian or parent must be obtained.
<br><br>
<U>Motorcycles with gear</U><br>
The candidate must be aged at least 18 years.
<br><br>
<U>Commercial Heavy Vehicles and Transport Vehicles</U><br>
The candidates must have finished at-least 8th standard of schooling and must be at least 18 years old.<br>
In some states, the minimum age requirement is 20 years.
<br><br><br>
<B><U>Details on Application Form</U></B><br><br>
1.Fill your name, address, date and place of birth<br>
2.Educational qualification<br>
3.Nationality (original documents)<br>
4.Blood group<br>
5.Previous driving licenses (if any)along with the details<br>
6.Driving school certificate (if required)<br>
7.Medical test exemption (if applicable)<br>
8.Declaration that particulars entered in the previous section are true, along with the signature of the parent/guardian of the applicant.<br>
<br><br>
<B><U>Documents</U></B><br><br>
To apply for Learner's License in India, the following documents will be required:<br><br>
1. Address proof (any one document from the listgiven below) :<br>
<UL style = "circle">
<LI>Recent telephone or electricity bill</LI>
<LI> LIC Bond</LI>
<LI>  Aadhaar Card</LI>
<LI> Passport</LI>
<LI>  Ration Card</LI>
<LI> If you are resident of some other city, then you can submit the House Rent Agreement with a copy of a recentutility bill, like electricity bill or gas bill as current address proof.</LI>
</UL>
<br>
2. Age Proof (any document from the following list) : <br>
<UL style = "circle">
<LI>PAN Card</LI>
<LI>Birth Certificate</LI>
<LI>Transfer certificate from school with date of birth mentioned on it</LI>
<LI>10th standard marksheet</LI>
<LI>Passport</LI>
</UL>
3. Passport-size photographs: at least 3 photographs<br>
4. Medical Certificate: To be signed by a certified Government doctor (if applicable)<br>
5. Form 1A <br>
6. Application form number 4: Completely filled (You can download the form from the state RTO portal or you can procure it from the nearest RTO office)<br><br><br>
